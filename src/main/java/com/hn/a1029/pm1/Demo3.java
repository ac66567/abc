package com.hn.a1029.pm1;

public class Demo3 {

    public static void main(String[] args) {

        //计算两个整数和
        int ab = sum(9, 19);
        System.out.println(ab);
    }

    // 返回值   参数    数学中函数   +   =
    public static int sum(int a, int b) {
        int ab = a + b;
        return ab;
    }

}
